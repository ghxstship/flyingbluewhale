#!/usr/bin/env node
// Generate src/lib/console/create-actions.generated.ts — the ⌘K "Create…"
// registry, derived from every static /console/**/new route (workflow audit
// F-A). Project-scoped creates (paths with a [param]) are excluded; they need
// a project picker (F-B) and are not global quick-creates.
//
//   node scripts/gen-create-actions.mjs           (write)
//   node scripts/gen-create-actions.mjs --check    (CI drift gate)

import { readdirSync, statSync, writeFileSync, readFileSync, existsSync, mkdirSync } from "node:fs";
import { join, relative } from "node:path";

const ROOT = process.cwd();
const CONSOLE = join(ROOT, "src/app/(platform)/console");
const OUT = join(ROOT, "src/lib/console/create-actions.generated.ts");

function walk(d) {
  const out = [];
  for (const n of readdirSync(d)) {
    const f = join(d, n);
    out.push(...(statSync(f).isDirectory() ? walk(f) : [f]));
  }
  return out;
}

// Title-case a kebab segment and singularize simply (label only; the curated
// entries in CommandPalette keep their hand-tuned i18n labels).
const ACRONYMS = { po: "PO", rfq: "RFQ", rfi: "RFI", dsar: "DSAR", ip: "IP", ros: "ROS", wip: "WIP", beo: "BEO", sop: "SOP", itb: "ITB", bim: "BIM", ai: "AI" };
const singular = (w) =>
  /ies$/.test(w) ? w.replace(/ies$/, "y") : /(ses|xes|ches|shes)$/.test(w) ? w.replace(/es$/, "") : /[^s]s$/.test(w) ? w.replace(/s$/, "") : w;
const titleSeg = (seg) =>
  seg
    .split("-")
    .map((w) => ACRONYMS[w] ?? w[0].toUpperCase() + w.slice(1))
    .join(" ");
const label = (resource) => {
  const parts = resource.split("/");
  const last = parts[parts.length - 1];
  return "New " + titleSeg(singular(last));
};

const news = walk(CONSOLE)
  .filter((f) => /\/new\/page\.tsx?$/.test(f))
  .map((f) => relative(CONSOLE, f).replace(/\\/g, "/").replace(/\/new\/page\.tsx?$/, ""))
  .filter((r) => !r.includes("[")) // exclude project-scoped (need a picker)
  .sort();

const actions = news.map((resource) => ({
  href: `/console/${resource}/new`,
  label: label(resource),
  group: titleSeg(resource.split("/")[0]), // domain group
}));

const body = `// GENERATED by scripts/gen-create-actions.mjs — do not edit.
// ⌘K "Create…" registry: every static /console/**/new route (workflow audit F-A).
// Regenerate: npm run gen:create-actions

export type CreateAction = { href: string; label: string; group: string };

export const CONSOLE_CREATE_ACTIONS: CreateAction[] = ${JSON.stringify(actions, null, 2)};
`;

if (process.argv.includes("--check")) {
  const cur = existsSync(OUT) ? readFileSync(OUT, "utf8") : "";
  if (cur !== body) {
    console.error(`✗ ${relative(ROOT, OUT)} is stale — run \`npm run gen:create-actions\` and commit.`);
    process.exit(1);
  }
  console.log(`✓ ${relative(ROOT, OUT)} up to date (${actions.length} create actions).`);
  process.exit(0);
}

mkdirSync(join(ROOT, "src/lib/console"), { recursive: true });
writeFileSync(OUT, body);
console.log(`✓ ${relative(ROOT, OUT)} — ${actions.length} create actions.`);
